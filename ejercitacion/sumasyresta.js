// Definimos una función llamada 'sumar'
function sumar(a, b) {
    return a + b;
  }
  
  // Definimos una función llamada 'restar'
  function restar(a, b) {
    return a - b;
  }
  
  // Exportamos ambas funciones para que estén disponibles fuera de este archivo
  module.exports = {
    sumar,
    restar
  };
  